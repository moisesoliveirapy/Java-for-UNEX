/*Você está ensinando uma criança a contar até 10. Não é uma atividade fácil, pois as crianças não
têm um senso claro de ordem. Assim como as crianças, os computadores não sabem contar,
então você precisa ensiná-los. Conte de 1 até 10.*/

public class questao1 {
    public static void main(String[] args) {
        System.out.println("Contando até 10");
        for (int i = 1; i <= 10; i++)   {
            System.out.println(i);
        }
    }
}